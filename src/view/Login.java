    private void entrar() {

        try {

            conecta.executaSQL("select *from usuario where user_usuario = '" + jTextFieldUsuario.getText() + "'");
            conecta.rs.first();
            if (conecta.rs.getString("user_senha").equals(jPasswordFieSenha.getText())) {
                TelaInicial tl = new TelaInicial(jTextFieldUsuario.getText());
                tl.setVisible(true);
                dispose();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Senha ou Usuario Invalido");
        }
    }

