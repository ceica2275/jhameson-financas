    public void preencherTabel(String Sql) {

        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"COD", "Tipo"}; //, "Valor", "Categoria", "Status"
        conecta.conexao();

        conecta.executaSQL(Sql);

        try {
            conecta.rs.first();
            do {

                dados.add(new Object[]{conecta.rs.getInt("id_transacao"), conecta.rs.getString("tipo_transacao")});

            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro na tabela Transação arraylist: " + ex);
        }

        ModelTabela model = new ModelTabela(dados, colunas);

        jTableTransacoes.setModel(model);

        jTableTransacoes.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTableTransacoes.getColumnModel().getColumn(0).setResizable(false);

        jTableTransacoes.getColumnModel().getColumn(1).setPreferredWidth(280);
        jTableTransacoes.getColumnModel().getColumn(1).setResizable(false);

        jTableTransacoes.getTableHeader().setReorderingAllowed(false);
        jTableTransacoes.setAutoResizeMode(jTableTransacoes.AUTO_RESIZE_OFF);
        jTableTransacoes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        conecta.desconecta();
    }

