        jTableDespesas.getColumnModel().getColumn(2).setPreferredWidth(100);
        jTableDespesas.getColumnModel().getColumn(2).setResizable(false);
        
        jTableDespesas.getColumnModel().getColumn(3).setPreferredWidth(50);
